bool resuelveCaso() {

    int N, L; cin >> N >> L;

    if (!std::cin) return false;

    int parches = 1, inicio = 0;
    for (int i = 0; i < N; i++){ 
        int x; cin >> x;
        if (inicio == 0) inicio = x;
        else {
            if (x > inicio + L) {
                parches++;
                inicio = x;
            }
        }
    }

    cout << parches << "\n";

    return true;
}

